package commandDesignPattern.approach1.customDevices;

public class GarageDoor {
    public void up() {
        System.out.println("open the Garage Door");
    }
    public void down() {
        System.out.println("close the Garage Door");
    }
    public void stop() {
        System.out.println("stop the Garage Door");
    }
    public void lightOn() {
        System.out.println("Turn the light on for the Garage Door");
    }
    public void lightOff() {
        System.out.println("Turn the light off for the Garage Door");
    }
}
